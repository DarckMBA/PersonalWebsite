function applyLanguage(lang) {
    const t = translations[lang];
    if (!t) return;

    // Nav-Bar Buttons
    aboutMeBtn.textContent = t.button1;
    projectsBtn.textContent = t.button2;
    contactBtn.textContent = t.button3;

    // Home Modal
    document.querySelector(".home-modal .modal-header h1").textContent = t.homeTitle;
    document.querySelector(".introduction h1").textContent = t.introTitle;
    document.querySelector(".introduction p").textContent = t.introText;
    document.querySelector(".education h1").textContent = t.eduTitle;
    document.querySelector(".education p").textContent = t.eduText;
    document.querySelector(".language h1").textContent = t.langTitle;
    document.querySelector(".language p").textContent = t.langText;

    // About Me Modal
    document.querySelector("#aboutModal .modal-header h1").textContent = t.aboutTitle;
    document.querySelector(".hobbies h1").textContent = t.hobbiesTitle;
    document.querySelector(".hobbies > p").textContent = t.hobbiesIntro;
    
    const hobbyItems = document.querySelectorAll(".hobbies-lists li");
    hobbyItems[0].textContent = t.hobby1;
    hobbyItems[1].textContent = t.hobby2;
    hobbyItems[2].textContent = t.hobby3;
    hobbyItems[3].textContent = t.hobby4;
    hobbyItems[4].textContent = t.hobby5;
    hobbyItems[5].textContent = t.hobby6;

    // Projects Modal
    document.querySelector("#projectsModal .modal-header h1").textContent = t.projectsTitle;
    document.querySelector(".finished-projects h1").textContent = t.finishedTitle;
    document.querySelector(".unfinished-projects h1").textContent = t.unfinishedTitle;
    document.querySelector(".finished-list li").textContent = t.minesweeper;
    document.querySelector(".unfinished-list li").textContent = t.zeteis;

    // Contact Modal
    document.querySelector("#contactModal .modal-header h1").textContent = t.contactTitle;
    document.querySelector(".contacts h1").textContent = t.contactSubtitle;

    // Remember Language
    localStorage.setItem("lang", lang);
}

// Attach click listeners to each language slider item
document.querySelectorAll(".language-slider .item").forEach((item) => {
    item.addEventListener("click", () => {
        const lang = item.dataset.lang;
        applyLanguage(lang);
    });
});

const savedLang = localStorage.getItem("lang");
if (savedLang) applyLanguage(savedLang);